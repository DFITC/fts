require(fGarch)
da=read.table("m-mrk4608.txt",head=T)
head(da)
dim(da)
mrk=as.numeric(as.character(da[2:752,2]))
head(mrk)
lnm=log(mrk+1)
acf(lnm)
Box.test(lnm,lag=12,type='Ljung')
at=lnm-mean(lnm)
Box.test(at^2,lag=12,type='Ljung')
acf(at)
acf(at^2)
acf(abs(at))
pacf(at^2)
m1=garchFit(~arma(0,1)+garch(3,0),data=lnm,trace=FALSE)
